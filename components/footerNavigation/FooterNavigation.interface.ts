/**
 * Интерфейс для компонента FooterNavigation.
 * Определяет структуру навигационных ссылок в подвале сайта.
 */
export interface FooterNavigationProps {
  /**
   * Массив навигационных ссылок.
   * Каждая ссылка содержит URL и текст для отображения.
   */
  links: Array<{
    /** URL-адрес ссылки */
    href: string;
    /** Текст ссылки для отображения */
    label: string;
  }>;
  /**
   * Дополнительные CSS-классы для контейнера навигации.
   * Позволяет кастомизировать стили извне.
   */
  className?: string;
  /**
   * ARIA-лейбл для навигационного элемента.
   * По умолчанию: "Навигация по сайту"
   */
  ariaLabel?: string;
}