/**
 * Свойства компонента двойного слайдера (RangeSlider).
 * Определяет параметры для выбора диапазона значений.
 */
export interface RangeSliderProps {
  /**
   * Минимально возможное значение диапазона (включительно).
   * Определяет нижнюю границу слайдера.
   * @example 0
   */
  min: number;

  /**
   * Максимально возможное значение диапазона (включительно).
   * Определяет верхнюю границу слайдера.
   * @example 1000
   */
  max: number;

  /**
   * Текущий выбранный диапазон в формате [минимальное значение, максимальное значение].
   * Оба значения должны находиться в пределах [min, max] и удовлетворять условию value[0] <= value[1].
   * @example [100, 500]
   */
  value: [number, number];

  /**
   * Функция обратного вызова, вызываемая при изменении диапазона пользователем.
   * Принимает новый диапазон [min, max].
   * @param value - Новый выбранный диапазон
   * @example (newRange) => setRange(newRange)
   */
  onChange: (value: [number, number]) => void;

  /**
   * Дополнительные CSS-классы для корневого элемента компонента.
   * Позволяет кастомизировать внешний вид слайдера.
   * @example 'my-slider custom-width'
   */
  className?: string;
}
