import { Review } from '@/interfaces/review.interface';

/**
 * Свойства компонента табов продукта (ProductTabs).
 * Определяет данные, необходимые для отображения вкладок "Описание" и "Отзывы".
 */
export interface ProductTabsProps {
  /**
   * Текст описания продукта.
   * Может содержать HTML-разметку для форматирования (например, теги <p>, <br>).
   * @example "<p>Этот продукт изготовлен из высококачественных материалов.</p>"
   */
  description: string;

  /**
   * Массив отзывов о продукте.
   * Каждый отзыв должен соответствовать интерфейсу `Review`.
   * Если отзывов нет, передаётся пустой массив.
   * @example [{ id: 1, author: 'Иван', rating: 5, text: 'Отличный товар!' }]
   */
  reviews: Review[];

  /**
   * Флаг загрузки данных.
   * Если true, компонент отображает скелетон вместо контента.
   * По умолчанию false.
   * @example true
   */
  loading?: boolean;
}
