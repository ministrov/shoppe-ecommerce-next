/**
 * Интерфейс пропсов компонента SocialsList.
 * Определяет параметры для настройки отображения списка социальных сетей.
 */
export interface SocialsListProps {
  /**
   * Дополнительный CSS-класс для контейнера списка.
   * Позволяет добавить пользовательские стили извне компонента.
   */
  className?: string;
  /**
   * Вариант отображения списка.
   * - `horizontal` — иконки расположены в строку (по умолчанию).
   * - `vertical` — иконки расположены в колонку.
   */
  variant?: 'horizontal' | 'vertical';
  /**
   * Размер иконок социальных сетей в пикселях.
   * По умолчанию 20.
   */
  iconSize?: number;
}